
public class WhileLoopExample {
	
	public static void main(String[] args) {
		System.out.println("Prog to illustrate while loop:");
		
		int i= 1;
		
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("Accending order");
		
		int j = 10;
		while(j > 0) {
			System.out.println(j);
			j--;
		}
		
	}

}
